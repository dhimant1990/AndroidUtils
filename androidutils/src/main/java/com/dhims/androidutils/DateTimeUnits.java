package com.dhims.androidutils;

/**
 * Define units for parsing time into string.
 *
 * @author Dhimant Desai (dhimant1990@gmail.com)
 */

public enum DateTimeUnits {
    /**
     * Days
     */
    DAYS,
    /**
     * Hours
     */
    HOURS,
    /**
     * Minutes
     */
    MINUTES,
    /**
     * Seconds
     */
    SECONDS,
    /**
     * Milliseconds
     */
    MILLISECONDS,
}